    t_b8 MakeBitVector(const t_size bit_cnt, s_bit_vector& o_bv, const s_allocator allocator) {
        ZF_ASSERT(bit_cnt > 0);

        s_array<t_u8> bytes;

        if (!AllocArray(BitsToBytes(bit_cnt), bytes, allocator)) {
            return false;
        }

        o_bv = {bytes, bit_cnt};

        return true;
    }
